package cim.example.demo2;

public class CourseOffered {

  private String name;
  private String level;

  // Constructore
  public CourseOffered(String name, String level) {
      this.name = name;
      this.name = level;
  }

  // Getter for name
  public String getName() {
    return name;
  }
  
  // Getter for level
  public String getlevel() {
    return level;
  }
  }
